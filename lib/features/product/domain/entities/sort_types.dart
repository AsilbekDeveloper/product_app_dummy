// Sorting types for products
enum SortType {
  priceLowToHigh, // Sort by price from low to high
  priceHighToLow, // Sort by price from high to low
  nameAZ, // Sort by name A -> Z
  nameZA, // Sort by name Z -> A
  ratingHighToLow, // Sort by rating high -> low
  ratingLowToHigh, // Sort by rating low -> high
  newestFirst, // Sort by newest products first
  oldestFirst, // Sort by oldest products first
}
